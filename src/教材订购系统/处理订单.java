    private void jb_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_confirmActionPerformed
        String currentbookId= this.jTextField1.getText();
        String number=jFormattedTextField1.getText();
        int orderNumber=Integer.parseInt(number);
        
        Connection con = null;
        try{
            con = db.getCon();
                            
            String isExisted=administrator.isExisted(con, currentbookId);

            if (字符串工具.isEmpty(currentbookId)) {
                fo.ShowInfo(this, "编号不能为空!");
                return;
            }
            else if (currentbookId == "") {
                JOptionPane.showMessageDialog(this, "请输入图书编号！");
                return;
            }
            else if (jFormattedTextField1.getText()=="") {
                JOptionPane.showMessageDialog(this, "请输入数量！");
                return;
            }
            else if(isExisted==""){
                JOptionPane.showMessageDialog(this, "图书编号输入有误，请重新输入！");
                jTextField1.setText("");
                jFormattedTextField1.setText("");
                return;
            }

            int n=JOptionPane.showConfirmDialog(this, "请您务必确保输入正确，操作后无法更改！点击“是”以输入订书单。");
            
            if(n==0)
            {
            try {
                administrator.setBookId(currentbookId);
                int book_capacity =co.QueryCapacity(con, currentbookId);

                try {
                    if (book_capacity - orderNumber >= 0){
                        con = database.getCon();

                        int NumSelectedAdd = administrator.NumSelectedAdd(con, currentbookId, orderNumber, book_capacity);
                        int OrderNormalState = administrator.OrderNormalState(con, currentbookId, orderNumber, book_capacity);
                        
                        if (NumSelectedAdd == 1 && OrderNormalState>=1) {
                            JOptionPane.showMessageDialog(this, "操作成功！");
                            jTextField1.setText("");
                            jFormattedTextField1.setText("");

                            this.fillTable(new Orderlist());
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "操作失败！");
                            jTextField1.setText("");
                            jFormattedTextField1.setText("");                          

                        }
                    }
                    else{
                        con = database.getCon();

                        int NumSelectedAdd = administrator.NumSelectedAdd(con, currentbookId, orderNumber, book_capacity);
                        int NumLackAdd = administrator.NumLackAdd(con, currentbookId, orderNumber, book_capacity);
                        int OrderNormalState = administrator.OrderNormalState(con, currentbookId, orderNumber, book_capacity);
                        int OrderStockoutState = administrator.OrderStockoutState(con, currentbookId);
                        
                        if (NumSelectedAdd >=0 && NumLackAdd == 1 && OrderNormalState>=0 && OrderStockoutState>=1) {
                            JOptionPane.showMessageDialog(this, "操作成功！此书涉及缺货，请等待进货方进货！");
                            jTextField1.setText("");
                            jFormattedTextField1.setText("");                            

                            this.fillTable(new Orderlist());
                        }
                        else {
                            JOptionPane.showMessageDialog(this, "操作失败！");
                            jTextField1.setText("");
                            jFormattedTextField1.setText("");                            

                        }
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "异常错误！");
                    jTextField1.setText("");
                    jFormattedTextField1.setText("");                    
                } finally {
                    try {
                        database.closeCon(con);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    database.closeCon(con);
                } catch (Exception e) {
                e.printStackTrace();
                }
            }
            }
        }catch (Exception e) {
                e.printStackTrace();
            } finally {
                try {
                    database.closeCon(con);
                } catch (Exception e) {
                e.printStackTrace();
                }
            }
    }//GEN-LAST:event_jb_confirmActionPerformed

