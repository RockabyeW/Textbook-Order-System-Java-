    private void jb_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_confirmActionPerformed
        String currentbookId= this.jTextField1.getText();
        Connection con = null;
        try{
            con = db.getCon();
                            
            String isExisted=selectbook.isExisted(con, currentbookId);

            if (字符串工具.isEmpty(currentbookId)) {
                fo.ShowInfo(this, "编号不能为空!");
                return;
            }
            else if (currentbookId == "") {
                JOptionPane.showMessageDialog(this, "请输入图书编号！");
                return;
            }
            else if(isExisted==""){
                JOptionPane.showMessageDialog(this, "图书编号输入有误，请重新输入！");
                jTextField1.setText("");
                return;
            }

            int book_capacity =co.QueryCapacity(con, currentbookId);
            int n;
            
            n = JOptionPane.showConfirmDialog(this, "确定要购买此书？");

            if (n == 0) {
                selectbook.setBookId(currentbookId);
                Orderlist orderlist=new Orderlist();
                Booklist booklist=new Booklist();
                Inventory inventory=new Inventory();
                String currentstudentId = 登录界面.currentStudent.getStudentId();

                try {
                        con = database.getCon();

                        String result=selectbook.isOrderExisted(con, currentbookId, currentstudentId);
                        
                        if (result != "" ) {
                            JOptionPane.showMessageDialog(this, "你已经选过这本书，不能重复选择！");
                            jTextField1.setText("");
                            return;
                        } 
                        
                        int SelectionAdd = selectbook.SelectionAdd(con, currentstudentId, currentbookId, book_capacity);
                        
                        if (SelectionAdd == 1) {
                            JOptionPane.showMessageDialog(this, "购买成功！请等待管理员确认订单状态！");
                            jTextField1.setText("");

                            this.fillTable(new Booklist());
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "购买失败！");
                            jTextField1.setText("");

                        }
                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "异常错误！");
                    jTextField1.setText("");
                } finally {
                    try {
                        database.closeCon(con);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }
        }catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                database.closeCon(con);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jb_confirmActionPerformed
    
