    private void fillTable(Booklist booklist) {
		DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
		dtm.setRowCount(0);
		Connection con = null;
		try {
			con = database.getCon();
			ResultSet rs = co.QueryAllBook(con);
                        
			while (rs.next()) {
				Vector v = new Vector();
				v.add(rs.getString("book_id"));
				v.add(rs.getString("book_name"));
                                v.add(rs.getString("book_press"));
				v.add(String.format("%1$.2f", rs.getFloat("book_price")));	
				dtm.addRow(v);
			}
		} catch (Exception e) {
			e.printStackTrace();
		} finally {
			try {
				database.closeCon(con);
			} catch (Exception e) {
				e.printStackTrace();
			}
		}
	}
