    private void jb_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_confirmActionPerformed
        String currentbookId= this.jTextField1.getText();
        String number=this.jTextField2.getText();
        int bookNumber=Integer.parseInt(number);
        Connection con = null;
        try{
            con = db.getCon();
                            
            String isExisted=administrator.isExisted(con, currentbookId);

            if (字符串工具.isEmpty(currentbookId)) {
                fo.ShowInfo(this, "编号不能为空!");
                return;
            }
            else if (currentbookId == "") {
                JOptionPane.showMessageDialog(this, "请输入图书编号！");
                return;
            }
            else if (this.jTextField2.getText() == "") {
                JOptionPane.showMessageDialog(this, "请输入数量！");
                return;
            }
            else if(isExisted==""){
                JOptionPane.showMessageDialog(this, "图书编号输入有误，请重新输入！");
                jTextField1.setText("");
                jTextField2.setText("");
                return;
            }

            JOptionPane.showConfirmDialog(this, "请您务必确保输入正确，操作后无法更改！点击“是”以输入订书单。");

            if (true) {
                administrator.setBookId(currentbookId);
                String currentadministratorId = 登录界面.currentAdministrator.getMId();

                try {
                        con = database.getCon();

                        int RestockingAdd = administrator.RestockingAdd(con, currentadministratorId, currentbookId, bookNumber);
                        int InventoryAdd = administrator.InventoryAdd(con, currentbookId, bookNumber);
                        int LackMinus = administrator.LackMinus(con,currentbookId, bookNumber);
                        int ChangeOrderState=administrator.ChangeOrderState(con, currentbookId, bookNumber);
                        
                        if (RestockingAdd == 1 && InventoryAdd==1 && LackMinus>=1 && ChangeOrderState>=1) {
                            JOptionPane.showMessageDialog(this, "输入成功！");
                            jTextField1.setText("");
                            jTextField2.setText("");

                            this.fillTable(new Lack());
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "输入失败！");
                            jTextField1.setText("");
                            jTextField2.setText("");
                        }

                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "异常错误！");
                    jTextField1.setText("");
                    jTextField2.setText("");
                } finally {
                    try {
                        database.closeCon(con);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }
        }catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                database.closeCon(con);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jb_confirmActionPerformed

