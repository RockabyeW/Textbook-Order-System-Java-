    private void fillTable(Lack lack) {
		DefaultTableModel dtm = (DefaultTableModel) lackTable.getModel();
		dtm.setRowCount(0);
		Connection con = null;
		try {
			con = database.getCon();
			ResultSet rs = administrator.QuerycurrentbookLackNumber(con);
			while (rs.next()) {
				Vector v = new Vector();
				v.add(rs.getString("book_id"));
                                v.add(rs.getString("book_name"));
				v.add(rs.getString("book_number"));
				dtm.addRow(v);
			}
		}catch (Exception e) {
			e.printStackTrace();
		} finally {
			try {
				database.closeCon(con);
			} catch (Exception e) {
				e.printStackTrace();
			}
		}
	}
